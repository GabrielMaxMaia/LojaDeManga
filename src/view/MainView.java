    public void puxaCliente(String cpf){
        System.out.println("Chamou o update");
        ClienteController ctr = ClienteController.getClienteController();
        Cliente cli = ctr.pesquisaPorCpf(cpf);
        if(cli != null ){
            if(abaAtual == 0){
                jTextFieldNomeClienteCart.setText(cli.getNome());
                jFormattedTextFieldCPFCart.setText(cli.getCpf());
            }else if(abaAtual == 2){
                jTextFieldCPFCadastro.setText(cli.getCpf());
                jTextFieldNomecadastro.setText(cli.getNome());
                jTextFieldEmailCadastro.setText(cli.getEmail());
                jTextFieldCelularCadastro.setText(cli.getCel());
                jTextFieldFixoCadastro.setText(cli.getTel());
                jTextFieldCEPCadastro.setText(cli.getCep());
                jTextFieldEnderecoCadastro.setText(cli.getEndereco());
                jTextFieldCidadeCadastro.setText(cli.getCidade());
                jTextFieldBairroCadastro.setText(cli.getBairro());
                jTextFieldComplemCadastro.setText(cli.getComplemento());
            }
        }else{
            jTextFieldNomecadastro.setText("CLIENTE NAO ENCONTRADO");
            System.out.println("Cli null");
        }
    }
    
